#include "minishell.h"

void	redirection_helper(char *str, int *i, int *count)
{
	if (str[(*i)] == '>' && str[(*i) + 1] == '>')
	{
		(*count)++;
		(*i) += 2;
	}
	else if (str[(*i)] == '<' && str[(*i) + 1] == '<')
	{
		(*count)++;
		(*i) += 2;
	}
}
